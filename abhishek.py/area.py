"area of rectangle - which accepts 2 parameter as length and width return as area of rectangle"
"area of circle - which accepts 1 parameter as radius return as area of circle"

def area_rectangle(length,width):
    """This a area of rectangle with parameter length and width and it will give area of rectangle """
    return  length * width

def area_ofcircle(radius):
    "This is a area of circle with paramter radius and it will give area of circle"
    return 3.14*radius*radius

